#include "libft.h"

bool    ft_allnum(char *s)
{
    int i;

    if (!s)
        return (false);
    i = 0;
    while (s[i])
    {
        if (s[i] < '0' || s[i] > '9')
            return (false);
        i++;
    }
    return (true);
}
